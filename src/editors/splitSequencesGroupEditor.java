    /**
     * This set the Properties
     * @param properties
     */
    public void setProperties(workflow_properties properties) {
        this.properties=properties;
        setTitle(properties.getName());       
        setSettingForProperties();        
    }

     

    /**
     * This set the different setting corresponding to the current properties
     */
    public void setSettingForProperties() {
           this.NamejTextField.setText(properties.getName());
           this.FilenamejTextField1.setText("");
           if (properties.isSet("criteria")) this.Criteria_jTextField.setText(properties.get("criteria"));
           if (properties.isSet("new_name")) this.FilenamejTextField1.setText(properties.get("new_name"));   
           this.jCheckBox1.setSelected(properties.getBoolean("preserve"));
           this.jCheckBox2.setSelected(properties.getBoolean("search_sequence_name"));
           this.jCheckBox3.setSelected(properties.getBoolean("search_sequence"));
           this.negative_jCheckBox4.setSelected(properties.getBoolean("negative_match"));
           //if (properties.isSet("Description")) this.Notice.setText(properties.get("Description"));
     }

