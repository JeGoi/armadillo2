    private void seed_TextFieldFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_seed_TextFieldFocusLost
      try {
        Integer seed=Integer.valueOf(this.seed_TextField.getText());
        if (seed%2==0) {
          //  MessageError("Warning, seed must be and odd number...","");
        } else properties.put("seed", seed);
      } catch(Exception e) {
        //MessageError("Warning, seed must be and odd number ex. 4533","");
        this.seed_TextField.setText(properties.get("default_seed"));
      }

    }//GEN-LAST:event_seed_TextFieldFocusLost

