    public void display(workflow_properties properties) {
        initComponents();
          DefaultComboBoxModel model=(DefaultComboBoxModel) this.jComboBox1.getModel();
        for (GeneticCode code:translator.code) {
            model.addElement(code.getName());
        }
        this.jComboBox1.setModel(model);


        setIconImage(Config.image);
        setProperties(properties);
        this.jTextArea1.setFont(new java.awt.Font(Font.MONOSPACED, Font.PLAIN, 11));
        // Set position 
        Dimension screenSize = Toolkit.getDefaultToolkit().getScreenSize();
        Dimension d = getSize();
        setLocation((screenSize.width-d.width)/2,
					(screenSize.height-d.height)/2);
        this.setAlwaysOnTop(true);
        this.setProperties(properties);
        this.setVisible(true);
    }

     public void saveImage(String filename) {
        BufferedImage bi;
        try {
            bi = new Robot().createScreenCapture(this.getBounds()); 
            ImageIO.write(bi, "png", new File(filename));
            this.setVisible(false);
        } catch (Exception ex) {
           Config.log("Unable to save "+filename+" dialog image");
        }            
    }
    
