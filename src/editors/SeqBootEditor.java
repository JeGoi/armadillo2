    /**
     * This set the Properties
     * @param properties
     */
    public void setProperties(workflow_properties properties) {
        this.properties=properties;
        setTitle(properties.getName());
        //if (properties.isSet("Description")) this.Notice.setText(properties.get("Description"));
        this.NamejTextField.setText(properties.getName());

        if (properties.isSet("mode")) {
           this.mode_ComboBox.setSelectedIndex(properties.getInt("mode"));
        } else {
            this.mode_ComboBox.setSelectedIndex(properties.getInt("default_mode"));
        }
        
        if (properties.isSet("type")) {
           this.type_ComboBox1.setSelectedIndex(properties.getInt("type"));
        } else {
            this.type_ComboBox1.setSelectedIndex(0);
        }
        
         if (properties.isSet("replicate")) {
            this.replicate_TextField.setText(properties.get("replicate"));
        } else {
            this.replicate_TextField.setText(properties.get("default_replicate"));
        }

        if (properties.isSet("seed")) {
            this.seed_TextField.setText(properties.get("seed"));
        } else {
            this.seed_TextField.setText(properties.get("default_seed"));
        }
    }


