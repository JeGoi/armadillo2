    /**
     * This set the Properties
     * @param properties
     */
    public void setProperties(workflow_properties properties) {
        this.properties=properties;
        setTitle(properties.getName());      
        this.NamejTextField.setText(properties.getName());
        //Config.log(this.properties.getProperties());
        if (properties.isSet("codeml.ctl")) this.jTextArea1.setText(properties.get("codeml.ctl"));
        this.jCheckBox2.setSelected(properties.getBoolean("do_not_run"));
        if (properties.isSet("preprocess_alignment")) {
            this.jCheckBox1.setSelected(properties.getBoolean("preprocess_alignment"));
        }
        //--Seqtype
        this.SequenceType_jComboBox.setSelectedIndex((properties.getInt("seqtype")==0?0:properties.getInt("seqtype")-1));
        if (this.SequenceType_jComboBox.getSelectedIndex()==0||this.SequenceType_jComboBox.getSelectedIndex()==2) {
            this.jCheckBox1.setEnabled(true);
            jComboBox1.setEnabled(true);
        }
         int code=properties.getInt("genetic_code");
        this.jComboBox1.setSelectedIndex(code);
         if (properties.isSet("Description")) this.Notice.setText(properties.get("Description"));
    }

      

  

