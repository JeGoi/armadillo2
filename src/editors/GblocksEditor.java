    private void MoltypejComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MoltypejComboBoxActionPerformed
        // 0 DNA
        // 1 Protein
        // 2 Codons        
        int type=this.MoltypejComboBox.getSelectedIndex();                
        properties.put("moltype",type);
        if (type==1) {
            this.UseSimilarity_jCheckBox1.setEnabled(true);
        } else {
            this.UseSimilarity_jCheckBox1.setEnabled(false);
            properties.remove("Use_Similarity_Matrices");
        }
        
    }//GEN-LAST:event_MoltypejComboBoxActionPerformed

