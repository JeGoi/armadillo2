    /**
     * This set the Properties
     * @param properties
     */
    public void setProperties(workflow_properties properties) {
        this.properties=properties;
        setTitle(properties.getName());
       
        this.NamejTextField.setText(properties.getName());

        if (properties.isSet("Maximum_Number_Of_Contiguous_Nonconserved_Positions")) {
           this.MaximumNonConserved_jComboBox.setSelectedItem(properties.get("Maximum_Number_Of_Contiguous_Nonconserved_Positions"));        
        } 
        
        if (properties.isSet("Minimum_Number_Of_Sequences_For_A_Conserved_Position")) {
           this.MinimumConserved_jComboBox.setSelectedItem(properties.get("Minimum_Number_Of_Sequences_For_A_Conserved_Position"));        
        } 
        //Minimum_Number_Of_Sequences_For_A_Flank_Position
        if (properties.isSet("Minimum_Number_Of_Sequences_For_A_Flank_Position")) {
           this.MinimumFlank_jComboBox.setSelectedItem(properties.get("Minimum_Number_Of_Sequences_For_A_Flank_Position"));        
        } 
        
        if (properties.isSet("Minimum_Length_Of_A_Block")) {
           this.MinimumLengthOfBlock_jComboBox.setSelectedItem(properties.get("Minimum_Length_Of_A_Block"));        
        } 
        //Allowed_Gap_Positions
        if (properties.isSet("Allowed_Gap_Positions")) {
           this.AllowedGap_jComboBox.setSelectedItem(properties.get("Allowed_Gap_Positions"));        
        } 
        if (properties.isSet("moltype")) {
           this.MoltypejComboBox.setSelectedIndex(properties.getInt("moltype"));
           if (properties.getInt("moltype")==1) {
               this.UseSimilarity_jCheckBox1.setEnabled(true);
           } else {
               this.UseSimilarity_jCheckBox1.setEnabled(false);
           }
        } else {
            properties.put("moltype", 0); //--By default set moltype to DNA (0)
        }
        if (properties.isSet("Use_Similarity_Matrices")) {
            this.UseSimilarity_jCheckBox1.setSelected(properties.getBoolean("Use_Similarity_Matrices"));           
        } else {
            this.UseSimilarity_jCheckBox1.setSelected(true);           
        }        

    }


