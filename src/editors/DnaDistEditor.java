    private void distGamma_ComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_distGamma_ComboBoxActionPerformed
      properties.put("gamma", this.distGamma_ComboBox.getSelectedIndex());
      //--Gamma rate
      if (this.distGamma_ComboBox.getSelectedIndex()==1) {
          this.alpha_TextField1.setEnabled(true);
          properties.put("alpha_gamma_rate", this.alpha_TextField1.getText());
      } else if (this.distGamma_ComboBox.getSelectedIndex()==2) {
          this.alpha_TextField1.setEnabled(true);
          this.invariant_TextField3.setEnabled(true);
          properties.put("alpha_gamma_rate", this.alpha_TextField1.getText());
          properties.put("gamma_invariant_site", this.invariant_TextField3.getText());
      } else {
          this.alpha_TextField1.setEnabled(false);
          this.invariant_TextField3.setEnabled(false);
          properties.remove("alpha_gamma_rate");
          properties.remove("gamma_invariant_site");
      }
}//GEN-LAST:event_distGamma_ComboBoxActionPerformed

