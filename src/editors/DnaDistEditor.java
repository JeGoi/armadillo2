    /**
     * This set the Properties
     * @param properties
     */
    public void setProperties(workflow_properties properties) {
        this.properties=properties;
        setTitle(properties.getName());       
        this.NamejTextField.setText(properties.getName());
        if (properties.isSet("model")) {
            this.ModeljComboBox.setSelectedIndex(properties.getInt("model"));
        }
        if (properties.isSet("gamma")) {
            this.distGamma_ComboBox.setSelectedIndex(properties.getInt("gamma"));
        }
         if (properties.isSet("transversion")) {
            this.distTrans_TextField.setText(properties.get("transversion"));
        }
        if (properties.isSet("CustomPhylipCommand")) {
            this.CustomPhylipjTextArea.setText(properties.get("CustomPhylipCommand"));
        }
        if (properties.isSet("alpha_gamma_rate")) {
            this.alpha_TextField1.setText(properties.get("alpha_gamma_rate"));
        }
        if (properties.isSet("gamma_invatiant_site")) {
            this.invariant_TextField3.setText(properties.get("gamma_invariant_site"));
        }
    }

      

