    /**
     * This set the Properties
     * @param properties
     */
    public void setProperties(workflow_properties properties) {
        this.properties=properties;
        setTitle(properties.getName());

        if (properties.isSet("gop")) {
            this.Gop_TextField.setText(properties.get("gop"));
        } else {
            this.Gop_TextField.setText(properties.get("default_mafft_gop"));
        }

         if (properties.isSet("gep")) {
            this.Gep_TextField.setText(properties.get("gep"));
        } else {
            this.Gep_TextField.setText(properties.get("default_mafft_gep"));
        }

        if (properties.isSet("tree_rebuilding")) {
            this.treerebuilding_jComboBox.setSelectedItem(properties.getInt("tree_rebuilding"));
        }
        
         if (properties.isSet("maxiters")) {
            this.treerebuilding_jComboBox.setSelectedItem(properties.getInt("maxiters"));
        }

        if (properties.isSet("fft")) {
            this.treerebuilding_jComboBox.setSelectedItem(properties.get("fft"));
        }

        if (properties.isSet("matrix")) {
            int index=Util.indexOf(properties.get("matrix"), realmatrixname);
            this.MatrixjComboBox.setSelectedIndex(index);
        }

        if (properties.isSet("moltype")) {
            String moltype=properties.get("moltype");
            if (moltype.equals("P")) this.kaling_MoltypejComboBox.setSelectedIndex(1);
            if (moltype.equals("N")) this.kaling_MoltypejComboBox.setSelectedIndex(0);
            this.MatrixjComboBox.setEnabled(moltype.equals("P"));
        }
        
        if (properties.isSet("JobId")) {
            this.JobIdjTextField.setText(properties.get("JobId"));
        } else this.JobIdjTextField.setText("");
       
        setTitle(properties.getName());      
        this.NamejTextField.setText(properties.getName());       
    }

